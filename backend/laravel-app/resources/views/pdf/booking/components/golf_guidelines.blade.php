        <h3 style="text-align: center; color: #4473c5;">Golf Cart Safety Guidelines</h3>

        <ul>
            <li>No one under the age of sixteen (16) may operate a golf cart.</li>

            <li>Golf cart operator are not permitted to drive while under influence of alcohol or drugs.</li>

            <li>Drive slowly and steady.</li>

            <li>Avoid passing to a deep stagnant water.</li>

            <li>Use extreme caution. Never drive faster than conditions allow.</li>

            <li>Be alert of road conditions, slowdown specifically uphill, downhill, and curves.</li>

            <li>Do not operate carts on lawns or landscaped areas.</li>

            <li>Watch for potholes, dips, and uneven surfaces.</li>

            <li>Avoid distractions, drivers should not use cellphones while operating cart.</li>

            <li>Never take your hands off the wheel.</li>

            <li>Do not allow more passengers than seating capacity or carry more load than the rated capacity.</li>

            <li>Guests are responsible for the ignition key the entire time they use the vehicle. Keys are not to be left in cart while unattended.</li>

            <li>Park in a designated parking space, do not block entrances to buildings, stairways, disability ramps.</li>

            <li>When the golf cart is not in use, make sure to engage the park brake lock and turn off the key.</li>

            <li>Discontinue use of golf cart if any problems occur.</li>

            <li>Report any maintenance problems such as brakes or leaks.</li>
        </ul>

