        <h3 style="text-transform: uppercase; text-align: center; margin-bottom: 48px;">Golf Policies and Guidelines</h3>

        <strong>Booking and Reservation</strong>
        <ol>
            <li>Booking and reservation should be based on available slot/tee time and Golf Carts.</li>
            <li>Walk-ins are discouraged, but will accept upon availability of slot/Tee time</li>
            <li>Walk-ins who wish to play beyond the default Tee off times can play, but advised of short play hours. Full rate.</li>
            <li>All are required to avail caddie services.</li>
            <li>Guest can book without golf carts.</li>
            <li>Maximum of 4 pax per flight can be booked.</li>
        </ol>

        <strong>Guest with Hotel Accommodation</strong>
        <ol>
            <li>Guest that will play during the day/time of his check out will be advised early checkout.</li>
            <li>Guest arriving before allowed check-in time will be assisted to G-lounge</li>
            <li>Guest arriving within allowed check-in time can either proceed directly to G-lounge or Hotel</li>
        </ol>

        <strong>Late Arrival</strong>
        <ol>
            <li>If guest is late, he can play either in the next available tee off time, or can play within his hole tee time</li>
        </ol>

        <strong>Non-Golf Players/ Gallery</strong>
        <ol>
            <li>DTT guest are not allowed in the Fairway. Allowed only at the gazebo area for picture taking and viewing.</li>
            <li>Galleries can accompany players in the course but to pay gallery fee.</li>
            <li>Children are not allowed in the golf course</li>
        </ol>

        <strong>Golf Lounge</strong>
        <ol>
            <li>Golf lounge is exclusive for golf players (Green Fee/Gallery Fee)</li>
            <li>Keys should be returned to caddie after play.</li>
        </ol>

        <strong>Food Services</strong>
        <ol>
            <li>Menus are provided per gazebo, they can call F&amp;B BPO for delivery (Lounge and Golf Course)</li>
            <li>Players can use G-lounge or Gazebos in Golf course for dining</li>
        </ol>

        <strong>Golf Cart</strong>
        <ol>
            <li>Only 2-seater golf carts can be used (or booked) for golf play.</li>
            <li>Tripping using golf carts are allowed inside the golf course only in case that no players are playing. An area for tripping is available at Gazebo at Hole No. 1.</li>
            <li>Park only at designated parking slots</li>
            <li>Use only at paved pathways.</li>
            <li>Use only within commercial area and golf course.</li>
        </ol>


        <strong>Golf Cart Rental</strong>
        <ol>
        <li>RE/DTT Guest can rent golf carts upon availability.</li>
        <li>Pay and Claim only at designated areas
            <ul>
                <li>Payment: Concierge Office</li>
                <li>Claim and return: Commercial Area at the back of Garden 2</li>
            </ul>
        </li>
        </ol>

